using namespace std;

int main(){
    cout<<"x y z w"<<endl;
    for(int(x)=0;x<=1;x++)
    for(int(y)=0;y<=1;y++)
    for(int(z)=0;z<=1;z++)
    {
        if (not(not(not(x==y<=z)))==1) {
            cout<<x<<" "<<y<<" "<<z<<" "<<"1"<<endl;
        }
        else cout<<x<<" "<<y<<" "<<z<<" "<<"0"<<endl;
    }
}